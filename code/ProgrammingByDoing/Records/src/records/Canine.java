package records;

/**
 *
 * @author lmgeorge <lauren.george@live.com>
 */
public class Canine {
  
  String name;
  String breed;
  String sex;
  int age;
  double weight;

  public String getBreed() {
    return breed;
  }

  public void setBreed(String breed) {
    this.breed = breed;
  }

  public int getAge() {
    return age;
  }

  public void setAge(int age) {
    this.age = age;
  }

  public double getWeight() {
    return weight;
  }

  public void setWeight(double weight) {
    this.weight = weight;
  }
  
}
