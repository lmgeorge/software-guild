package com.swcguild.luckysevens;

/**
 *
 * @author lmgeorge <lauren.george@live.com>
 */
public class Game {
	private int rollCounter;
	private int maxAmountRoll;
	private int maxAmountBet;

	public int getRollCounter() {
		return rollCounter;
	}

	public void setRollCounter(int rollCounter) {
		this.rollCounter = rollCounter;
	}

	public int getMaxAmountRoll() {
		return maxAmountRoll;
	}

	public void setMaxAmountRoll(int maxAmountRoll) {
		this.maxAmountRoll = maxAmountRoll;
	}

	public int getMaxAmountBet() {
		return maxAmountBet;
	}

	public void setMaxAmountBet(int maxAmountBet) {
		this.maxAmountBet = maxAmountBet;
	}
	
	
	
}
